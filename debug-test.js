// Simple debug script to check what getGoalDateRange returns
function getGoalDateRange(period) {
  const now = new Date();
  let startDate, endDate;

  switch (period) {
    case 'weekly':
      startDate = new Date(now);
      const day = startDate.getDay();
      const diff = startDate.getDate() - day + (day === 0 ? -6 : 1);
      startDate.setDate(diff);
      startDate.setHours(0, 0, 0, 0);
      
      endDate = new Date(startDate);
      endDate.setDate(startDate.getDate() + 6);
      endDate.setHours(23, 59, 59, 999);
      break;

    case 'monthly':
      startDate = new Date(now.getFullYear(), now.getMonth(), 1);
      endDate = new Date(now.getFullYear(), now.getMonth() + 1, 0);
      endDate.setHours(23, 59, 59, 999);
      break;

    case 'yearly':
      startDate = new Date(now.getFullYear(), 0, 1);
      endDate = new Date(now.getFullYear(), 11, 31);
      endDate.setHours(23, 59, 59, 999);
      break;
  }

  return {
    startDate: startDate.toISOString().split('T')[0],
    endDate: endDate.toISOString().split('T')[0],
  };
}

console.log('Today:', new Date().toISOString().split('T')[0]);
console.log('Weekly:', getGoalDateRange('weekly'));
console.log('Monthly:', getGoalDateRange('monthly'));
console.log('Yearly:', getGoalDateRange('yearly'));